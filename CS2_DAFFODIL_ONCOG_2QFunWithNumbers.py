# Fun With Numbers App
print("Welcome to Fun With Numbers!")

# Step 1: Ask for the user's age
age = int(input("Hi there! Please enter your age: "))

# Step 2: Initialize sum variable
total_sum = 0

# Step 3: Use a loop to add numbers from 1 to age
for number in range(1, age + 1):
    total_sum += number

# Step 4: Display the result
print(f"The sum of all numbers from 1 to {age} is: {total_sum} .Inspiring message: Every loop is a quiet push forward. Like life we grow, one step at a time.") 